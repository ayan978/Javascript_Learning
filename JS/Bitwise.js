
document.write(5 & 1,"<br>") //0101 & 0001 = 0001 (result=1)
document.write(5 | 1,"<br>") //0101 | 0001 = 0101 (result = 5)
document.write(~ 5,"<br>") //NOT operation, ~0101 = 1010 (result=10)
document.write(5<<1,"<br>") //Left shift, 0101<<1 = 1010
document.write(5^1,"<br>") //XOR, 0101^0001 = 0100 (result 4)
document.write(5>>1,"<br>") //Right shift by pushing copies of the leftmost bit in from the left
// 0101>>1 = 0010 (result 2)
document.write(5>>>1,"<br>") //Right shift by pushing zeros in from the left
//0101>>>1 = 0010


let d = new Date()
document.write(d.getFullYear()+"<br>") //Returns full year
document.write(d.getMonth()+"<br>") //Returns month assuming january = 0 and onwards
document.write(d.getDate()+"<br>") //Returns Date
document.write(d.getHours()+"<br>") //Return Hour
document.write(d.getMinutes()+"<br>") //Returns minute
document.write(d.getSeconds()+"<br>") //Returns Second
document.write(d.millisecond()+"<br>") //Return Milliscond
document.write(d.getTime()+"<br>") //Returns millisecond since January 01,1970
document.write(d.getDay()+"<br>") //Return Day assuming Sunday = 0
document.write(Date.now()) //Returns present time
